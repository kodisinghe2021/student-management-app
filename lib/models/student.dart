class Student {
  String id;
  String firstName;
  String lastName;
  String birthday;
  String age;
  String gender;
  String mobile;
  String location;
  String school;
  String email;

  Student({
    required this.id,
    required this.firstName,
    required this.lastName,
    required this.birthday,
    required this.age,
    required this.gender,
    required this.mobile,
    required this.location,
    required this.school,
    required this.email,
  });
}
